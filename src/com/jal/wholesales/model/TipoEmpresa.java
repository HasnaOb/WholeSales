package com.jal.wholesales.model;

public class TipoEmpresa {
	private long id;
	private String nombre;
	public TipoEmpresa () {
		
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
